public static void dealCards(ArrayList<ImageIcon> hand, ImageIcon[][] cards, boolean[][] dealt) {
        while(hand.size()<5) {
            Random rand = new Random();
            int x = rand.nextInt(4);
            int y = rand.nextInt(13);
            if(dealt[x][y] == false) {
                hand.add(cards[x][y]);
                dealt[x][y] = true;
            }
        }
    }

    public static String card(int i, int y) {
        String id = "";
        if(i == 0) {
            id = "d";
        }else if(i == 1) {
            id = "c";
        }else if(i == 2) {
            id = "h";
        }else {
            id = "s";
        }
        return id+y;
    }
